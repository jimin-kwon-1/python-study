"""
BOJ 1546 - 평균
분류: 수학 / 리스트 / 평균 변환
━━━━━━━━━━━━━━━━━━━━━━━━━━
[문제 요약]
- N개의 시험 점수가 주어진다.
- 가장 높은 점수를 기준으로 모든 점수를 새 점수로 변환한다.
  새 점수 = (원래 점수 / 최고 점수) x 100
- 변환된 점수들의 평균을 구한다.
━━━━━━━━━━━━━━━━━━━━━━━━━━
[핵심 개념]
1) 최고 점수 먼저 구하기
   → max(scores)

2) 모든 점수를 변환
   → score / max_score * 100

3) 변환된 점수들의 평균 계산
※ 단순 평균 X. 변환 후 평균 ✔
━━━━━━━━━━━━━━━━━━━━━━━━━━
[헷갈렸던 부분]
- 최고 점수를 따로 입력받는 게 아님 → 점수 리스트 안에서 찾아야 함
- max를 변수명으로 사용하면 안됨 → 파이썬 내장함수 max() 덮어씀
━━━━━━━━━━━━━━━━━━━━━━━━━━
[패턴 정리]
"값 변환 후 평균"
- 먼저 기준(max) 찾기
- 모든 값을 변환
- 평균 계산

"""

# 시험 개수 입력
N = int(input())

# 점수 리스트 입력
scores = list(map(int, input().split()))

# 최고 점수 찾기
max_score = max(scores)

# 변환된 점수 총합 계산
total = 0
for score in scores:
    total += score / max_score * 100

# 평균 출력
print(total / N)