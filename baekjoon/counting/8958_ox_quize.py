"""
BOJ 8958 - OX퀴즈
분류: 구현 / 문자열 / 누적합(연속 카운팅)
난이도: 기초
━━━━━━━━━━━━━━━━━━━━━━━━━━
[문제 요약]
- 테스트 케이스 T개가 주어진다.
- 각 줄은 'O'와 'X'로만 이루어진 문자열.
- 'O'는 점수를 얻고, 연속된 'O'일수록 점수가 1,2,3... 누적된다.
- 'X'가 나오면 연속 카운트는 0으로 초기화된다.
- 각 테스트 케이스마다 총점을 한 줄에 출력.
━━━━━━━━━━━━━━━━━━━━━━━━━━
[핵심 개념]
1) 문자열 입력은 그대로 받는다: s = input()
2) streak(연속 O 개수) 변수로 연속 길이를 추적
   - O면 streak += 1
   - X면 streak = 0
3) score(총점)에 매번 streak를 더한다
   - O일 때 score += streak
━━━━━━━━━━━━━━━━━━━━━━━━━━
[헷갈렸던 부분]
- for ch in s (문자 순회)가 테스트케이스 반복문 바깥으로 나가면
  마지막 문자열 1개만 계산하게 됨 → 들여쓰기 위치가 핵심.
━━━━━━━━━━━━━━━━━━━━━━━━━━
[패턴 정리]
"연속 길이 누적 점수" 패턴
- streak(연속 길이) + score(누적합)
- X 같은 '끊기는 조건'에서 streak를 0으로 초기화
- 비슷한 유형: 연속 1의 길이, 최장 연속 구간, DP 기초 누적 등

"""

T = int(input()) # 테스트 케이스의 개수

for _ in range(T):
    s = input().strip() # OX 문자열 입력 받기 
                        # (문자열 입력 문제) input().strip() → 문자열 양쪽의 공백/ 개행(\n) 제거
    streak = 0 # 연속 O의 개수
    score = 0

    for ch in s:
        if ch == 'O':
            streak += 1
            score += streak
        else:
            streak = 0

    print(score)