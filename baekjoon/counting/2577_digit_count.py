"""
BOJ 2577 - 숫자의 개수

[문제 요약]
- A, B, C 세 개의 정수를 입력받는다.
- A * B * C의 결과에서
  0~9까지 각 숫자가 몇 번 등장하는지 출력한다.

[핵심 개념]
1. 숫자를 문자열로 변환하면 각 자리 접근이 쉬워진다.
   → result = str(A*B*C)

2. 카운팅 배열 생성
   → count = [0] * 10
   인덱스 0~9가 각각 숫자 0~9의 개수를 의미한다.

3. 인덱스를 이용한 빈도 증가
   → count[int(ch)] += 1
   ch는 문자이므로 int로 변환 후 인덱스 접근

4. 출력은 0부터 9까지 순서대로 한 줄씩 출력

[헷갈렸던 부분]
- [0]*10 로 0이 10개 만들어지는지 이해 부족
- count[int(ch)] 가 왜 가능한지 (인덱스 개념)
- 숫자 상태에서 처리하려다가 복잡해질 뻔함
  → 문자열로 바꾸는 게 훨씬 단순

[패턴 정리]
이 문제는 "빈도수 카운팅 패턴" 문제다.
→ 리스트 인덱스를 숫자와 직접 연결하는 구조.
→ 이후 알파벳 카운팅 문제에도 그대로 사용 가능.
"""

A = int(input())
B = int(input())
C = int(input())

result = str(A * B * C) # 문자열로 바꿔서 문자 하나씩 비교

count = [0] * 10 # 0을 10번 복사해서 카운팅 배열 리스트 생성

for ch in result:
    count[int(ch)] += 1 # (ex) ch가 '7'이면 count[7]에 1을 더한다

for num in count:
    print(num)