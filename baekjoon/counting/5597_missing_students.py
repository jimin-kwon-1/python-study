"""
BOJ 5597 - 과제 안 내신 분..?
분류: 구현 / 리스트 / 카운팅 / 누락 찾기
━━━━━━━━━━━━━━━━━━━━━━━━━━
[문제 요약]
- 학생 번호 1~30
- 과제 제출 학생 28명 입력
- 제출하지 않은 학생 2명 출력
━━━━━━━━━━━━━━━━━━━━━━━━━━
[핵심 개념]
1) 학생 번호 범위
   → 1~30

2) 제출 여부 기록
   → 리스트 활용

3) 제출하지 않은 번호 찾기
━━━━━━━━━━━━━━━━━━━━━━━━━━
[패턴 정리]
"전체 범위에서 빠진 값 찾기"

구조
- 전체 리스트 생성
- 입력 받은 번호 표시
- 표시 안 된 값 출력

"""

# 학생 제출 여부 리스트
students = [0] * 31

# 제출한 학생 입력
for _ in range(28):
    n = int(input())
    students[n] = 1

# 제출하지 않은 학생 찾기
for i in range(1, 31):
    if students[i] == 0:
        print(i)