"""
BOJ 10807 - 개수 세기
분류: 구현 / 리스트 / 카운팅
━━━━━━━━━━━━━━━━━━━━━━━━━━
[문제 요약]
- N개의 정수가 주어진다.
- 정수 v가 리스트에서 몇 번 등장하는지 출력한다.
━━━━━━━━━━━━━━━━━━━━━━━━━━
[핵심 개념]
1) 리스트 입력
   → list(map(int, input().split()))

2) 특정 값 등장 횟수 세기
   → 반복문 + 조건문

3) 조건 만족 시 카운트 증가
   → cnt += 1
━━━━━━━━━━━━━━━━━━━━━━━━━━
[패턴 정리]
"조건 만족 개수 세기"

구조
- cnt = 0
- 반복문
- 조건 만족 시 cnt += 1

이 패턴은 다음 문제들에도 반복됨:
- 특정 숫자 개수
- 특정 문자 개수
- 조건 만족 원소 개수
"""

# 정수 개수 입력
N = int(input())
# 정수 리스트 입력
nums = list(map(int, input().split()))
# 찾는 값 입력
v = int(input())

# 등장 횟수 카운트
cnt = 0
for num in nums:
    if num == v:
        cnt+=1

# 결과 출력
print(cnt)