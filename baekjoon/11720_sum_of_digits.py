"""
BOJ 11720 - 숫자의 합
분류: 구현 / 문자열 / 합
━━━━━━━━━━━━━━━━━━━━━━━━━━
[문제 요약]
- 숫자의 개수 N이 주어진다.
- N개의 숫자가 공백 없이 붙어서 입력된다.
- 각 숫자를 더한 값을 출력한다.
━━━━━━━━━━━━━━━━━━━━━━━━━━
[핵심 개념]

1. 숫자를 문자열로 입력 받기
→ input()

2. 문자열을 하나씩 순회
→ for ch in s

3. 문자 → 숫자 변환
→ int(ch)

4. 합 누적
→ total += int(ch)
━━━━━━━━━━━━━━━━━━━━━━━━━━
[패턴 정리]

"문자열 숫자 합"

구조
for ch in s:
    total += int(ch)

- 공백 있으면 split()
- 붙어있으면 문자열 순회
"""

# 숫자의 개수
N = int(input())

# 숫자 문자열
s = input()

total = 0

# 문자열 순회
for ch in s:
    total += int(ch)

# 결과 출력
print(total)