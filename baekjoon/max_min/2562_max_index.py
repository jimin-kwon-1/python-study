"""
BOJ 2562 - 최댓값
분류: 구현 / 최댓값 탐색
━━━━━━━━━━━━━━━━━━━━━━━━━━
[문제 요약]
- 9개의 자연수가 주어진다.
- 그 중 최댓값을 찾는다.
- 그 값이 몇 번째 수인지(1부터 시작) 출력한다.
━━━━━━━━━━━━━━━━━━━━━━━━━━
[핵심 개념]
1) 최댓값 탐색 패턴
   → max_val 변수 사용

2) 위치 저장 필요
   → max_idx 변수 사용

3) 첫 입력을 초기값으로 설정
   → 이후 값들과 비교하면서 갱신
━━━━━━━━━━━━━━━━━━━━━━━━━━
[헷갈렸던 부분]
- 인덱스는 0이 아니라 1부터 시작
- 초기 max_val을 0으로 두면 안됨
  → 첫 입력값을 기준으로 잡아야 안전
━━━━━━━━━━━━━━━━━━━━━━━━━━
[패턴 정리]
"최댓값 + 위치 찾기"
- 반복하면서
  더 크면 → 값 갱신 + 위치 갱신

"""

# 첫 번째 입력을 기준값으로 설정
max_val = int(input())  # 현재까지의 최댓값
max_idx = 1             # 최댓값의 위치 (1번째)

# 나머지 8개 입력 처리
for i in range(2, 10):  # 2번째부터 9번째까지
    n = int(input())

    # 현재 값이 더 크면 최댓값과 위치 갱신
    if n > max_val:
        max_val = n
        max_idx = i

# 결과 출력
print(max_val)
print(max_idx)
