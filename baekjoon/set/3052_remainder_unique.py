"""
BOJ 3052 - 나머지
분류: 구현 / 수학 / set(중복 제거, 유니크 개수)
난이도: 기초
━━━━━━━━━━━━━━━━━━━━━━━━━━
[문제 요약]
- 서로 다른 10개의 수(또는 10개의 정수)가 입력으로 주어진다.
- 각 수를 42로 나눈 나머지를 구한다.
- 그 나머지들 중 "서로 다른 값"이 몇 개인지 출력한다.
━━━━━━━━━━━━━━━━━━━━━━━━━━
[핵심 개념]
1) 나머지 연산
- n % 42 : 42로 나눈 나머지(0~41)

2) set(집합)으로 중복 제거
- set은 같은 값을 여러 번 넣어도 1개만 유지한다.
- 따라서 "서로 다른 나머지"를 구할 때 사용

3) 유니크 개수 = len(set)
- seen에 나머지를 넣고, 마지막에 len(seen) 출력
━━━━━━━━━━━━━━━━━━━━━━━━━━
[헷갈렸던 부분]
- 단순히 cnt를 올리면 중복을 걸러낼 방법이 없음
  → "서로 다른/중복 제거/종류" 키워드가 보이면 set 떠올리기
- set에 넣을 때 원본 n이 아니라 n % 42 를 넣어야 함
━━━━━━━━━━━━━━━━━━━━━━━━━━
[패턴 정리]
"유니크 개수(set) 패턴"
- seen = set()
- 반복하면서 key/value를 seen.add(...)
- print(len(seen))

비슷한 문제:
- 서로 다른 값의 개수
- 종류 개수
- 중복 제거 후 카운트

"""
seen = set() # 중복을 자동으로 제거

for _ in range(10):
    n = int(input())
    seen.add(n % 42)

print(len(seen))
